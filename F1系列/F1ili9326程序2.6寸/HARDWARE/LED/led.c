/************显示字符************/
void showchar(u8 a,u8 b,u8 num)
{
	u8 i=0,j=0,temp=0;
	LCD_qu(a,b,a+7,b+15);
	LCD_WR_REG_DATA(0x0200,a);    //GRAM address set   设置地址计数器的初始值    
	LCD_WR_REG_DATA(0x0201,b);
	LCD_WR_REG(0x202);
	for(j=0;j<16;j++)
	{
		temp = PIC3[num][j];  //提取对应字符字模数据
		for(i=0;i<8;i++)     //八位二进制数据
		{
			if(temp&0x01) LCD_WR_DATA(red);
			else LCD_WR_DATA(0x0000);
			temp >>= 1;
		}
	}
}

