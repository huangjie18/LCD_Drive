void LCD_Clear()
{
	u16 length,hight;
	LCD_WR_REG(0X2A00);
	LCD_WR_DATA(0);
	LCD_WR_REG(0X2A01);
	LCD_WR_DATA(0);
	LCD_WR_REG(0X2B00);
	LCD_WR_DATA(0);
	LCD_WR_REG(0X2B01);
	LCD_WR_DATA(0);
	
	LCD_WR_REG(0X2C00);
	for(hight=0;hight<800;hight++)
	{
		for(length=0;length<480;length++)
		{
			LCD_WR_DATA(0x001F);
		}
	}

}

u16 LCD_Read(u16 LCD_Reg)
{
	u8 Reg_i;
	u16 temp;
	LCD_WR_REG(LCD_Reg);
	LCD_IN();
	LCD_RS = 1;
	LCD_CS = 0;
	LCD_RD = 0;
	
	delay_us(2);
	LCD_id = GPIOE->IDR;
	for(Reg_i=0;Reg_i<16;Reg_i++)
	{
		switch(Reg_i)
		{
			case 0: temp = temp|LCD_DB15_IN;break; 
			case 1: temp = (temp<<1)|LCD_DB14_IN;break; 
			case 2: temp = (temp<<1)|LCD_DB13_IN;break; 
			case 3: temp = (temp<<1)|LCD_DB12_IN;break; 
			case 4: temp = (temp<<1)|LCD_DB11_IN;break; 
			case 5: temp = (temp<<1)|LCD_DB10_IN;break; 
			case 6: temp = (temp<<1)|LCD_DB9_IN;break; 
			case 7: temp = (temp<<1)|LCD_DB8_IN;break; 
			case 8: temp = (temp<<1)|LCD_DB7_IN;break; 
			case 9: temp = (temp<<1)|LCD_DB6_IN;break; 
			case 10:temp = (temp<<1)|LCD_DB5_IN;break; 
			case 11:temp = (temp<<1)|LCD_DB4_IN;break; 
			case 12:temp = (temp<<1)|LCD_DB3_IN;break; 
			case 13:temp = (temp<<1)|LCD_DB2_IN;break; 
			case 14:temp = (temp<<1)|LCD_DB1_IN;break; 
			case 15:temp = (temp<<1)|LCD_DB0_IN;break;
			
		}
	}
	LCD_RD = 1;
	LCD_CS = 1;
	LCD_OUT();
	return temp;
}

