/*
文件说明：该LCD驱动适合正点原子F1和F4开发板使用
文件使用：适合8线并口传输，只需要修改LCD_Init初始驱动
		  接线按照lcd.h里的定义接
 */
void LCD_Writ_Bus(int data)  //并行数据写入函数
{
	u8 i,temp;
	LCD_CS = 0;
	for(i = 0; i < 16; i++)
	{
		temp = (data>>i)&1;
		switch(i)
		{
			case 0:  LCD_DB0 = temp;break;
			case 1:  LCD_DB1 = temp;break;
			case 2:  LCD_DB2 = temp;break;
			case 3:  LCD_DB3 = temp;break;
			case 4:  LCD_DB4 = temp;break;
			case 5:  LCD_DB5 = temp;break;
			case 6:  LCD_DB6 = temp;break;
			case 7:  LCD_DB7 = temp;break;
			case 8:  LCD_DB8 = temp;break;
			case 9:  LCD_DB9 = temp;break;
			case 10: LCD_DB10 = temp;break;
			case 11: LCD_DB11 = temp;break;
			case 12: LCD_DB12 = temp;break;
			case 13: LCD_DB13 = temp;break;
			case 14: LCD_DB14 = temp;break;
			case 15: LCD_DB15 = temp;break;
		}           
	}
	LCD_WR = 0;
	LCD_WR = 1;
	LCD_CS = 1;
}

/***************************写数据**********************************************/
void LCD_WR_DATA(int da)
{
	LCD_RS = 1;
	LCD_Writ_Bus(da);
}
/***************************写命令**************************************************/
void LCD_WR_REG(int reg)
{
	LCD_RS = 0;
	LCD_Writ_Bus(reg);
}
/****************************写命令和数据**********************************************/
void LCD_WR_REG_DATA(int reg, int da)
{
	LCD_WR_REG(reg);
	LCD_WR_DATA(da);
}


void GPIO_5510_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOB|RCC_AHB1Periph_GPIOD|RCC_AHB1Periph_GPIOE
	|RCC_AHB1Periph_GPIOF|RCC_AHB1Periph_GPIOG, ENABLE);
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;
	GPIO_Init(GPIOB, &GPIO_InitStructure);
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_7|GPIO_Pin_8|GPIO_Pin_9|GPIO_Pin_10|GPIO_Pin_11|
	GPIO_Pin_12|GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15;
	GPIO_Init(GPIOE, &GPIO_InitStructure);
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_4|GPIO_Pin_5|GPIO_Pin_14|GPIO_Pin_15
	|GPIO_Pin_0|GPIO_Pin_1|GPIO_Pin_8|GPIO_Pin_9|GPIO_Pin_10;
	GPIO_Init(GPIOD, &GPIO_InitStructure);
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12;
	GPIO_Init(GPIOF, &GPIO_InitStructure);

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12;
	GPIO_Init(GPIOG, &GPIO_InitStructure);	
	
	LCD_CS = 1;
	LCD_RD = 1;
	LCD_BL = 1;
	LCD_WR = 1;
}
